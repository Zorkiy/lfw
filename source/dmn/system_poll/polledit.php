  // Выставляем уровень обработки ошибок 
  // (http://www.softtime.ru/info/articlephp.php?id_article=23)
  error_reporting(E_ALL & ~E_NOTICE);

  // Устанавливаем соединение с базой данных
  require_once("../../config/config.php");
  // Подключаем блок авторизации
  require_once("../utils/security_mod.php");
  // Подключаем классы формы
  require_once("../../config/class.config.dmn.php");

  try
  {
    // Защищаем GET-параметр от SQL-инъекции
    $_GET['id_catalog'] = intval($_GET['id_catalog']);
    if(empty($_POST))
    {
      $query = "SELECT * FROM $tbl_poll
                WHERE id_catalog=$_GET[id_catalog]
                LIMIT 1";
      $cat = mysql_query($query);
      if(!$cat)
      {
        throw new ExceptionMySQL(mysql_error(), 
                                 $query,
                                "Ошибка при обращении 
                                 к голосованию");
      }
      $_REQUEST = mysql_fetch_array($cat);
      $_REQUEST['page'] = $_GET['page'];
      if($_REQUEST['hide'] == 'show') $_REQUEST['hide'] = true;
      else $_REQUEST['hide'] = false;
      if($_REQUEST['archive'] == 'active') $_REQUEST['archive'] = true;
      else $_REQUEST['archive'] = false;
    }
    $name = new field_textarea("name",
                               "Вопрос",
                                true,
                                $_REQUEST['name']);
    $hide = new field_checkbox("hide",
                               "Отображать",
                                $_REQUEST['hide']);
    $archive = new field_checkbox("archive",
                                  "Сделать активным",
                                  $_REQUEST['archive']);
    $page = new field_hidden_int("page",
                                 false,
                                 $_REQUEST['page']);
    $id_catalog = new field_hidden_int("id_catalog",
                                 true,
                                 $_REQUEST['id_catalog']);
  
    $form = new form(array("name"       => $name, 
                           "hide"       => $hide,
                           "archive"    => $archive,
                           "page"       => $page,
                           "id_catalog" => $id_catalog), 
                     "Редактировать",
                     "field");
    // Обработчик HTML-формы
    if(!empty($_POST))
    {
      // Проверяем корректность заполнения HTML-формы
      // и обрабатываем текстовые поля
      $error = $form->check();
      if(empty($error))
      {
        // Скрытая или открытая позциция
        if($form->fields['hide']->value) $showhide = "show";
        else $showhide = "hide";
        // Активное голосование или архивное
        if($form->fields['archive']->value)
        {
          // Голосование активное - все существующие
          // голосования переводим в пассивный режим
          $query = "UPDATE $tbl_poll SET archive = 'archive'";
          if(!mysql_query($query))
          {
            throw new ExceptionMySQL(mysql_error(), 
                                     $query,
                                    "Ошибка обновления 
                                     статуса позиций");
          }
          $status = "active";
        }
        else $status = "archive";
        // Формируем SQL-запрос на добавление
        // новостного сообщения
        $query = "UPDATE $tbl_poll
                  SET name = '{$form->fields[name]->value}',
                      archive = '$status',
                      hide = '$showhide'
                  WHERE id_catalog = {$form->fields[id_catalog]->value}";
        if(!mysql_query($query))
        {
          throw new ExceptionMySQL(mysql_error(), 
                                   $query,
                                  "Ошибка при редактировании
                                   позиции");
        }
        // Осуществляем перенаправление
        // на главную страницу администрирования
        header("Location: index.php?page={$form->fields[page]->value}");
        exit();
      }
    }
    // Начало страницы
    $title     = 'Редактирование позиции';
    $pageinfo  = '<p class=help></p>';
    // Включаем заголовок страницы
    require_once("../utils/top.php");
    
    echo "<p><a href=# onClick='history.back()'>Назад</a></p>";
    // Выводим сообщения об ошибках, если они имеются
    if(!empty($error))
    {
      foreach($error as $err)
      {
        echo "<span style=\"color:red\">$err</span><br>";
      }
    }
    // Выводим HTML-форму 
    $form->print_form();
  }
  catch(ExceptionObject $exc) 
  {
    require("../utils/exception_object.php"); 
  }
  catch(ExceptionMySQL $exc)
  {
    require("../utils/exception_mysql.php"); 
  }
  catch(ExceptionMember $exc)
  {
    require("../utils/exception_member.php"); 
  }

  // Включаем завершение страницы
  require_once("../utils/bottom.php");
?>
